def display_rainbow(rainbow)
  
  rainbow_abbrev = ["R","O","Y","G","B","I","V"]
  
  puts("#{rainbow_abbrev[0]}: #{rainbow[0]}, #{rainbow_abbrev[1]}: #{rainbow[1]}, #{rainbow_abbrev[2]}: #{rainbow[2]}, #{rainbow_abbrev[3]}: #{rainbow[3]}, #{rainbow_abbrev[4]}: #{rainbow[4]}, #{rainbow_abbrev[5]}: #{rainbow[5]}, #{rainbow_abbrev[6]}: #{rainbow[6]}")
  
  
end


display_rainbow(['red', 'orange', 'yellow', 'green', 'blue', 'indigo', 'violet'])
